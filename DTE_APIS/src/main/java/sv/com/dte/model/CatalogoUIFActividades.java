package sv.com.dte.model;

public class CatalogoUIFActividades  { 
    private String codeGiroUIF;
    private String CodeUIF;
    private String codeT24;
    private String nombreCategActivi;
    private String estado;
    
	public String getCodeGiroUIF() {
		return codeGiroUIF;
	}
	public void setCodeGiroUIF(String codeGiroUIF) {
		this.codeGiroUIF = codeGiroUIF;
	}
	public String getCodeUIF() {
		return CodeUIF;
	}
	public void setCodeUIF(String codeUIF) {
		CodeUIF = codeUIF;
	}
	public String getCodeT24() {
		return codeT24;
	}
	public void setCodeT24(String codeT24) {
		this.codeT24 = codeT24;
	}
	public String getNombreCategActivi() {
		return nombreCategActivi;
	}
	public void setNombreCategActivi(String nombreCategActivi) {
		this.nombreCategActivi = nombreCategActivi;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
    
}
